public class bigger_equal_smaller
{
    public static void main(String[] args)
    {
    	int a = 5;
    	int b = 4;
    	if(a > b) 
    	{
    		System.out.println("Bigger");
    	}
    	else if(a == b)
    	{
    		System.out.println("Equal");
    	}
    	else
    	{
    		System.out.println("Smaller");
    	}
    }
}